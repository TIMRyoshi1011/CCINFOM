DROP DATABASE IF EXISTS THEATERSHOWS;
CREATE DATABASE IF NOT EXISTS `THEATERSHOWS`;
USE `THEATERSHOWS`;

CREATE TABLE `customers` (
  `CUSTOMER_ID` VARCHAR(8),
  `FIRST_NAME` VARCHAR(50),
  `LAST_NAME` VARCHAR(50),
  `PHONE_NUMBER` VARCHAR(50),
  `EMAIL_ADDRESS` VARCHAR(50),
  PRIMARY KEY (`CUSTOMER_ID`)
);

CREATE TABLE `theaters` (
  `THEATER_ID` VARCHAR(8),
  `THEATER_NAME` VARCHAR(20),
  `CAPACITY` INT,
  `MAX_ROWS` INT,
  `MAX_COLS` INT,
  `THEATER_STATUS` VARCHAR(20),
  PRIMARY KEY (`THEATER_ID`)
);

CREATE TABLE `shows` (
  `SHOW_ID` VARCHAR(8),
  `TITLE` VARCHAR(50),
  `RUNTIME` TIME,
  `SHOW_PRICE` INT,
  `STATUS` VARCHAR(50),
  PRIMARY KEY (SHOW_ID)
);

CREATE TABLE `theater_shows` (
  `THEATER_SHOW_ID` VARCHAR(8),
  `THEATER_ID` VARCHAR(8),
  `SHOW_ID` VARCHAR(8),
  `START_TIME` TIME,
  `END_TIME` TIME,
  `SHOW_STATUS` VARCHAR(50),
  `RESERVATION_DATE` DATE,
  `AUDIENCE_TURNOUT` DECIMAL(5,2),
  PRIMARY KEY (THEATER_SHOW_ID),
  FOREIGN KEY (THEATER_ID)
      REFERENCES theaters(THEATER_ID),
  FOREIGN KEY (SHOW_ID)
      REFERENCES shows(SHOW_ID)
);

CREATE TABLE `booking` (
  `BOOKING_ID` VARCHAR(8),
  `CUSTOMER_ID` VARCHAR(8),
  `THEATER_SHOW_ID` VARCHAR(8),
  `NOOFTICKETS` INT,
  `TOTAL_PRICE` INT,
  `BOOKING_STATUS` VARCHAR(50),
  `BOOKING_DATE` DATE,
  PRIMARY KEY (BOOKING_ID),
  FOREIGN KEY (CUSTOMER_ID)
      REFERENCES customers(CUSTOMER_ID),
  FOREIGN KEY (THEATER_SHOW_ID)
      REFERENCES theater_shows(THEATER_SHOW_ID)
);

CREATE TABLE `seat` (
  `SEAT_ID` VARCHAR(8),
  `THEATER_ID` VARCHAR(8),
  `ROW_NO` INT,
  `COL_NO` INT,
  `STATUS` VARCHAR(50),
  PRIMARY KEY (SEAT_ID),
  FOREIGN KEY (THEATER_ID)
      REFERENCES theaters(THEATER_ID)
);

CREATE TABLE `seat_booking` (
  `BOOKING_ID` VARCHAR(8),
  `SEAT_ID` VARCHAR(8),
  PRIMARY KEY (BOOKING_ID, SEAT_ID),
  FOREIGN KEY (BOOKING_ID)
      REFERENCES booking(BOOKING_ID),
  FOREIGN KEY (SEAT_ID)
      REFERENCES seat(SEAT_ID)
);

CREATE TABLE `staff` (
  `STAFF_ID` VARCHAR(8),
  `FIRST_NAME` VARCHAR(50),
  `LAST_NAME` VARCHAR(50),
  `POSITION` VARCHAR(50),
  `EMPLOYMENT_STATUS` VARCHAR(50),
  `SALARY` INT,
  PRIMARY KEY (STAFF_ID)
);

CREATE TABLE `staff_assignment` (
  `THEATER_SHOW_ID` VARCHAR(8),
  `STAFF_ID` VARCHAR(8),
  PRIMARY KEY (STAFF_ID, THEATER_SHOW_ID),
  FOREIGN KEY (STAFF_ID)
      REFERENCES staff(STAFF_ID),
  FOREIGN KEY (THEATER_SHOW_ID)
      REFERENCES theater_shows(THEATER_SHOW_ID)
);

-- for customers
DELIMITER //

CREATE TRIGGER before_insert_customers
BEFORE INSERT ON customers
FOR EACH ROW
BEGIN
    DECLARE max_id INT;

    -- Get the numeric part of the largest CUSTOMER_ID
    SELECT IFNULL(MAX(CAST(SUBSTRING(CUSTOMER_ID, 3) AS UNSIGNED)), 0)
    INTO max_id
    FROM customers;

    -- Generate the new CUSTOMER_ID
    SET NEW.CUSTOMER_ID = CONCAT('CT', LPAD(max_id + 1, 6, '0'));
END //

DELIMITER ;

